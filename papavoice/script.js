// State management
let currentLanguage = 'zh';
let mediaRecorder = null;
let recordingChunks = [];
let recordingStartTime = null;
let recordingIntervalId = null;

// Initialize app
document.addEventListener('DOMContentLoaded', () => {
    initializeServiceWorker();
    loadSettings();
    setupEventListeners();
    updateLanguage(currentLanguage);
    updatePrenatalContent();
    updateStatistics();
    initializeAudioContext();
});

// Service Worker registration
function initializeServiceWorker() {
    if ('serviceWorker' in navigator) {
        navigator.serviceWorker.register('service-worker.js')
            .then(reg => console.log('Service Worker registered'))
            .catch(err => console.log('Service Worker registration failed', err));
    }
}

// Load settings from localStorage
function loadSettings() {
    const settings = JSON.parse(localStorage.getItem('papavoice-settings')) || {};
    
    // Apply autoplay setting
    const autoplay = settings.autoplay !== false;
    const autoplayToggle = document.getElementById('toggle-autoplay');
    if (autoplay) {
        autoplayToggle.classList.add('active');
    }
    
    // Apply notifications setting
    const notifications = settings.notifications !== false;
    const notificationsToggle = document.getElementById('toggle-notifications');
    if (notifications) {
        notificationsToggle.classList.add('active');
    }
    
    // Apply dark mode setting
    const darkMode = settings.darkMode === true;
    const darkModeToggle = document.getElementById('toggle-darkmode');
    if (darkMode) {
        darkModeToggle.classList.add('active');
        applyDarkMode(true);
    }
}

// Save settings to localStorage
function saveSettings() {
    const settings = {
        autoplay: document.getElementById('toggle-autoplay').classList.contains('active'),
        notifications: document.getElementById('toggle-notifications').classList.contains('active'),
        darkMode: document.getElementById('toggle-darkmode').classList.contains('active')
    };
    localStorage.setItem('papavoice-settings', JSON.stringify(settings));
}

// Apply dark mode
function applyDarkMode(enable) {
    if (enable) {
        document.documentElement.style.filter = 'invert(1) hue-rotate(180deg)';
    } else {
        document.documentElement.style.filter = 'none';
    }
}

// Setup event listeners
function setupEventListeners() {
    // Language selector
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');
            currentLanguage = e.target.dataset.lang;
            updateLanguage(currentLanguage);
            updatePrenatalContent();
            updateStatistics();
        });
    });

    // Recording buttons
    document.getElementById('btn-start-record').addEventListener('click', startRecording);
    document.getElementById('btn-stop-record').addEventListener('click', stopRecording);

    // Playback buttons
    document.getElementById('btn-speak-prenatal').addEventListener('click', speakPrenatalContent);
    document.getElementById('btn-play-recording').addEventListener('click', playLatestRecording);

    // Settings toggles
    document.getElementById('toggle-autoplay').addEventListener('click', (e) => {
        e.target.classList.toggle('active');
        saveSettings();
    });

    document.getElementById('toggle-notifications').addEventListener('click', (e) => {
        e.target.classList.toggle('active');
        saveSettings();
    });

    document.getElementById('toggle-darkmode').addEventListener('click', (e) => {
        e.target.classList.toggle('active');
        applyDarkMode(e.target.classList.contains('active'));
        saveSettings();
    });

    // Data management
    document.getElementById('btn-export-data').addEventListener('click', exportData);
    document.getElementById('btn-clear-data').addEventListener('click', clearAllData);

    // Bottom navigation
    document.querySelectorAll('.nav-item').forEach(item => {
        item.addEventListener('click', (e) => {
            const tabId = item.dataset.tab;
            switchTab(tabId);
        });
    });
}

// Initialize audio context
function initializeAudioContext() {
    if (navigator.mediaDevices && navigator.mediaDevices.getUserMedia) {
        // Audio context is ready for recording
    }
}

// Start recording
async function startRecording() {
    try {
        const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
        mediaRecorder = new MediaRecorder(stream);
        recordingChunks = [];
        recordingStartTime = Date.now();
        
        mediaRecorder.addEventListener('dataavailable', (e) => {
            recordingChunks.push(e.data);
        });

        mediaRecorder.addEventListener('stop', () => {
            const recordingBlob = new Blob(recordingChunks, { type: 'audio/wav' });
            saveRecording(recordingBlob);
            stream.getTracks().forEach(track => track.stop());
        });

        mediaRecorder.start();
        updateRecordingUI(true);
        startRecordingTimer();
        
        document.getElementById('btn-start-record').disabled = true;
        document.getElementById('btn-stop-record').disabled = false;
    } catch (err) {
        console.error('Recording error:', err);
        alert('Unable to access microphone. Please check permissions.');
    }
}

// Stop recording
function stopRecording() {
    if (mediaRecorder && mediaRecorder.state !== 'inactive') {
        mediaRecorder.stop();
        updateRecordingUI(false);
        clearRecordingTimer();
        document.getElementById('btn-start-record').disabled = false;
        document.getElementById('btn-stop-record').disabled = true;
    }
}

// Update recording UI
function updateRecordingUI(isRecording) {
    const indicator = document.getElementById('recording-indicator');
    if (isRecording) {
        indicator.style.display = 'flex';
    } else {
        indicator.style.display = 'none';
    }
}

// Recording timer
function startRecordingTimer() {
    let seconds = 0;
    recordingIntervalId = setInterval(() => {
        seconds++;
        const mins = Math.floor(seconds / 60);
        const secs = seconds % 60;
        document.getElementById('recording-time').textContent = 
            `${String(mins).padStart(2, '0')}:${String(secs).padStart(2, '0')}`;
    }, 1000);
}

function clearRecordingTimer() {
    clearInterval(recordingIntervalId);
    document.getElementById('recording-time').textContent = '00:00';
}

// Save recording to localStorage
function saveRecording(blob) {
    const reader = new FileReader();
    reader.onload = (e) => {
        const recordings = JSON.parse(localStorage.getItem('papavoice-recordings')) || [];
        const recording = {
            id: Date.now(),
            name: `Recording ${recordings.length + 1}`,
            data: e.target.result,
            timestamp: new Date().toLocaleString(currentLanguage === 'zh' ? 'zh-CN' : 'en-US'),
            duration: Math.round((Date.now() - recordingStartTime) / 1000)
        };
        recordings.push(recording);
        localStorage.setItem('papavoice-recordings', JSON.stringify(recordings));
        updateRecordingsList();
        updateStatistics();
    };
    reader.readAsArrayBuffer(blob);
}

// Update recordings list
function updateRecordingsList() {
    const recordings = JSON.parse(localStorage.getItem('papavoice-recordings')) || [];
    const list = document.getElementById('recordings-list');
    list.innerHTML = '';
    
    recordings.reverse().forEach((recording, index) => {
        const item = document.createElement('div');
        item.className = 'recording-item';
        item.innerHTML = `
            <div class="recording-item-info">
                <div class="recording-item-name">${recording.name}</div>
                <div class="recording-item-meta">${recording.timestamp} (${recording.duration}s)</div>
            </div>
            <div class="recording-item-controls">
                <button class="btn-primary" onclick="playRecording(${recording.id})">▶️</button>
                <button class="btn-danger" onclick="deleteRecording(${recording.id})">🗑️</button>
            </div>
        `;
        list.appendChild(item);
    });
}

// Play recording
function playRecording(recordingId) {
    const recordings = JSON.parse(localStorage.getItem('papavoice-recordings')) || [];
    const recording = recordings.find(r => r.id === recordingId);
    
    if (recording) {
        const audio = new Audio();
        audio.src = recording.data;
        audio.play();
        recordPlayback(recording.name);
    }
}

// Delete recording
function deleteRecording(recordingId) {
    let recordings = JSON.parse(localStorage.getItem('papavoice-recordings')) || [];
    recordings = recordings.filter(r => r.id !== recordingId);
    localStorage.setItem('papavoice-recordings', JSON.stringify(recordings));
    updateRecordingsList();
    updateStatistics();
}

// Play prenatal content using Web Speech API
function speakPrenatalContent() {
    const content = getPrenatalContentText();
    if ('speechSynthesis' in window) {
        speechSynthesis.cancel();
        const utterance = new SpeechSynthesisUtterance(content);
        utterance.lang = getLanguageCode(currentLanguage);
        utterance.rate = 0.9;
        speechSynthesis.speak(utterance);
        recordPlayback('Prenatal Content');
    }
}

// Play latest recording
function playLatestRecording() {
    const recordings = JSON.parse(localStorage.getItem('papavoice-recordings')) || [];
    if (recordings.length > 0) {
        playRecording(recordings[recordings.length - 1].id);
    } else {
        alert('No recordings found');
    }
}

// Get prenatal content
function getPrenatalContentText() {
    const contents = [
        translations[currentLanguage]['prenatal-content-1'],
        translations[currentLanguage]['prenatal-content-2'],
        translations[currentLanguage]['prenatal-content-3'],
        translations[currentLanguage]['prenatal-content-4']
    ];
    return contents.join(' ');
}

// Get language code for speech synthesis
function getLanguageCode(lang) {
    const codes = {
        zh: 'zh-CN',
        en: 'en-US',
        ja: 'ja-JP',
        ko: 'ko-KR'
    };
    return codes[lang] || 'en-US';
}

// Record playback statistics
function recordPlayback(contentName) {
    const stats = JSON.parse(localStorage.getItem('papavoice-stats')) || {
        totalPlays: 0,
        lastPlayed: null,
        lastPlayedContent: null
    };
    stats.totalPlays++;
    stats.lastPlayed = new Date().toLocaleString(currentLanguage === 'zh' ? 'zh-CN' : 'en-US');
    stats.lastPlayedContent = contentName;
    localStorage.setItem('papavoice-stats', JSON.stringify(stats));
    updateStatistics();
}

// Update statistics display
function updateStatistics() {
    const stats = JSON.parse(localStorage.getItem('papavoice-stats')) || {
        totalPlays: 0,
        lastPlayed: null,
        lastPlayedContent: null
    };
    const recordings = JSON.parse(localStorage.getItem('papavoice-recordings')) || [];
    
    document.getElementById('stat-plays').textContent = stats.totalPlays;
    document.getElementById('stat-recordings').textContent = recordings.length;
    
    if (stats.lastPlayed) {
        document.getElementById('last-play-content').innerHTML = `
            <strong>${stats.lastPlayedContent}</strong><br>
            ${stats.lastPlayed}
        `;
    } else {
        document.getElementById('last-play-content').textContent = translations[currentLanguage]['last-play-content'];
    }
}

// Update prenatal content display
function updatePrenatalContent() {
    const list = document.getElementById('prenatal-list');
    list.innerHTML = '';
    
    for (let i = 1; i <= 4; i++) {
        const contentKey = `prenatal-content-${i}`;
        const content = translations[currentLanguage][contentKey];
        const div = document.createElement('div');
        div.className = 'prenatal-content';
        div.innerHTML = `<p>${content}</p>`;
        list.appendChild(div);
    }
}

// Update language
function updateLanguage(lang) {
    Object.keys(translations[lang]).forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            element.textContent = translations[lang][key];
        }
    });
}

// Switch tabs
function switchTab(tabId) {
    document.querySelectorAll('.tab-content').forEach(tab => tab.classList.remove('active'));
    document.getElementById(tabId).classList.add('active');
    
    document.querySelectorAll('.nav-item').forEach(item => item.classList.remove('active'));
    document.querySelector(`[data-tab="${tabId}"]`).classList.add('active');
    
    if (tabId === 'tab-record') {
        updateRecordingsList();
    }
}

// Export data
function exportData() {
    const stats = localStorage.getItem('papavoice-stats');
    const recordings = localStorage.getItem('papavoice-recordings');
    const settings = localStorage.getItem('papavoice-settings');
    
    const data = {
        stats: JSON.parse(stats || '{}'),
        recordingsCount: JSON.parse(recordings || '[]').length,
        settings: JSON.parse(settings || '{}')
    };
    
    const dataStr = JSON.stringify(data, null, 2);
    const blob = new Blob([dataStr], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `papavoice-data-${new Date().toISOString()}.json`;
    a.click();
    URL.revokeObjectURL(url);
}

// Clear all data
function clearAllData() {
    if (confirm('Are you sure? This will delete all recordings and statistics.')) {
        localStorage.removeItem('papavoice-recordings');
        localStorage.removeItem('papavoice-stats');
        updateRecordingsList();
        updateStatistics();
    }
}
